# Programa: Ejemplo de caracteres especiales en Python

# Salto de linea \n
mensaje1 = "Hola\nMundo"
print(mensaje1)

# Tabulacion \t
mensaje2 = "\tJuan"
print(mensaje2)

# Comillas dentro de cadena
mensaje3 = "Ella dijo: \"Hola a todos\" "
print(mensaje3)

# Barra invertida \
mensaje4 = "C:\\Usuarios\\Marce\\Documentos"
print(mensaje4)

# Cadenas cruda (raw string) r lleva un r delante y esto le dira a python que debe interpretar la cadena de forma literal
mensaje5 = r"C:\Usuarios\Marce\Documentos"
print(mensaje5)